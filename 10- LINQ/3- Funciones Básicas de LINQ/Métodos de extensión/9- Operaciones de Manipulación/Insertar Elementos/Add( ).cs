/*El método Add se utiliza para insertar un elemento al final de una colección que implementa la interfaz IList. Aquí tienes un ejemplo de cómo usar Add:*/

var numbers = new List<int> { 1, 2, 3, 4, 5 };

numbers.Add(6);

Console.WriteLine(string.Join(", ", numbers)); // Resultado: 1, 2, 3, 4, 5, 6

/*Explicacion

En este ejemplo, hemos declarado una lista de números y utilizamos el método Add para insertar el número 6 al final de la lista.
