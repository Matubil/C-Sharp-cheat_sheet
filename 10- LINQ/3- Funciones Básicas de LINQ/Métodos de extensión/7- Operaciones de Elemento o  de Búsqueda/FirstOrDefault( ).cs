
/*El operador `FirstOrDefault()` nos permite obtener el primer elemento de una colección que cumple con una 
condición determinada. Si no se encuentra ningún elemento que cumpla la condición, se devuelve el valor predeterminado
 del tipo de elemento. Aquí tienes un ejemplo:*/

var result = collection.FirstOrDefault(element => element.Property == value);


/*Este código busca el primer elemento de la colección que tenga la propiedad Property igual al valor `value`.*/