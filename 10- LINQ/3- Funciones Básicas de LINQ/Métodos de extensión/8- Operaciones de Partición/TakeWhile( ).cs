/* El operador TakeWhile() nos permite tomar elementos desde el inicio de una colección mientras se cumpla una 
condición determinada. Aquí tienes un ejemplo:*/

var result = collection.TakeWhile(element => condition);

/*Este código toma elementos desde el inicio de la colección mientras se cumpla la condición especificada y
 los devuelve como un nuevo subconjunto.*/