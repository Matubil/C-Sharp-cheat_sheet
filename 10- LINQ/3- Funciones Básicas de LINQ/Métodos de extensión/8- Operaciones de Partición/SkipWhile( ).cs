/*El operador SkipWhile() nos permite saltar elementos desde el inicio de una colección hasta que se cumpla 
una condición determinada, y tomar el resto de elementos. Aquí tienes un ejemplo:*/

var result = collection.SkipWhile(element => condition);

/*Este código salta elementos desde el inicio de la colección hasta que se cumpla la condición especificada y devuelve 
el resto de elementos como un nuevo subconjunto.*/