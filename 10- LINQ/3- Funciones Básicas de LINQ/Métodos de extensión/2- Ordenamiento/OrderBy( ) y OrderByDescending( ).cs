/*Los métodos de extensión OrderBy() y OrderByDescending() se pueden utilizar para ordenar los 
elementos de una colección en base a ciertos criterios. Aquí tienes un ejemplo:*/

var result = collection.OrderBy(element => element.Property);

/*Este código devuelve una nueva colección con los elementos ordenados de forma ascendente según la propiedad
 element.Property. Para ordenar de forma descendente, puedes utilizar el método OrderByDescending().*/