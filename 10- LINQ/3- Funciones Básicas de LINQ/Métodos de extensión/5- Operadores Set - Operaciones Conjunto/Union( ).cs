/*El operador Union() nos permite combinar dos colecciones en una nueva colección que contiene todos los 
elementos distintos de ambas colecciones. Aquí tienes un ejemplo:*/

var result = collection1.Union(collection2);

/*Este código combina collection1 y collection2, eliminando cualquier duplicado y devolviendo una nueva
 colección que contiene todos los elementos distintos de ambas colecciones.*/